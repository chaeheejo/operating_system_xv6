int
sys_kill(void)
{
  int pid;

  if(argint(0, &pid) < 0)
    return -1;
  return kill(pid);
}

int
sys_getpid(void)
{
  return myproc()->pid;
}

int
sys_sbrk(void)
{
  int addr;
  int n;

  if(argint(0, &n) < 0)
    return -1;
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

int
sys_sleep(void)
{
  int n;
  uint ticks0;

  if(argint(0, &n) < 0)
    return -1;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(myproc()->killed){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

// return how many clock tick interrupts have occurred
// since start.
int
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

int
sys_weightset(void) //프로세스의 weight 값을 설정하는 시스템 콜
{
  int weight;
  
  //매개변수로 전달받은 weight 값을 읽어온 뒤
  if(argint(0, &weight)<0){
    return -1;
  }
  //0보다 작거나 같으면 에러 처리를 해준다.
  if(weight<=0){
    return -1;
  }
   
  //proc.c 파일에 구현한 do_weightset 함수를 호출해 weight 값을 현재 프로세스의 weight에 할당해준다.
  do_weightset(weight);
  return 0;
}